package com.pvetec.inspectra.enums;

/**
 * Represents the possible statuses of a test item.
 */
public enum TestStatus {
    /**
     * Indicates that the test item has passed the test.
     */
    PASSED,

    /**
     * Indicates that the test item has failed the test.
     */
    FAILED,

    /**
     * Indicates that the test item has not been tested yet.
     */
    NOT_TESTED
}
